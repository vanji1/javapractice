package InterviewQuestions;

public class PrintLoopImaBatman {

	public static void main(String[] args) {

		for (int i=5;i>=0;i--) {
			System.out.println("I am a BatMan");
			System.out.println();
		}
		
		for (int j=5;j>=0;j--) {
			System.out.println("I am a BatMan"+" "+j);
			System.out.println();
		}
	}

}
