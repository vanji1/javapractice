package InterviewQuestions;

public class PrintNumbersWhile {

	public static void main(String[] args) {

		int n=10;
		while(n!=0) {
			System.out.print(n+" ");
			n--;
		}
		int i=10;
		while(i!=0) {
			System.out.println("Hello World");
			i--;
		}
		System.out.println("*****************************");
		int k=1;
		while(k!=7) {
			System.out.println(k);
			k++;
		}
	}

}
