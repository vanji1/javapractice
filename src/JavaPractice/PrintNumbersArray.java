package JavaPractice;

public class PrintNumbersArray {

	public static void main(String[] args) {
		
		// print numbers like 
		//00 01 02 03 04 05
		//10 11 12 13 14 15
		
		for(int i=0;i<=9;i++) {
			for(int j=0;j<=9;j++) {
			System.out.print(i+ ""+j+ " ");
		}
			System.out.println();

	}
	}
}
